package day11;

public class LogicalNotOperators {
    public static void main(String[] args) {

        System.out.println(true);
        System.out.println(false);
        System.out.println(! true); //  flip the coin and make it false
        System.out.println(! false); // flip the coin and make it true


        boolean isRecording = false;
        System.out.println(! isRecording);// ! makes the result change to be oppositre

    }
}
