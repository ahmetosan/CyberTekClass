package day18;

public class ForLoopIntro {
    public static void main(String[] args) {


        // count 1 to 5
        //  fpr loop() has 3 parts separaed by
                // it will run only once when you enter the loop
        // second part termination condition
                // loop will stop if it's false
                // it will be checked everytime

        // third part increment ot decrement to change condition eventually

        // it will run each iteration


        for (int i = 1; i <= 5; i++  ){
            System.out.println("i = "+ i);
            System.out.println("happy birth day");
        }

    }
}